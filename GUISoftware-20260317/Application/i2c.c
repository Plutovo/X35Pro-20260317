void    StartEERAM(void)
{
    SCL_LOW;
    __no_operation();
    __no_operation();
    SDA_HIGH;
    __no_operation();
    __no_operation();
    SCL_HIGH;
    __no_operation();
    __no_operation();
    SDA_LOW;
    __no_operation();
    __no_operation();
    SCL_LOW;
    __no_operation();
    __no_operation();
}
