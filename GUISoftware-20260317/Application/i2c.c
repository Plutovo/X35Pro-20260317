void    RTEEPROMwrite(INT16U addr, INT8U databyte)
{
    INT8U temp_l,temp_h;
    WriteEnable();
    temp_l=(INT8U)(addr&0xFF);
    temp_h=(INT8U)((addr>>8)&0xFF);
    StartEERAM();
    OutputData(IIC_WRITE);
    AckEERAM();
    OutputData(temp_h);
    AckEERAM();
    OutputData(temp_l);
    AckEERAM();
    OutputData(databyte);
    StopEERAM();
    WriteProtect();
}
/*
********************************************************************************
츰냔：
묘콘췄甘：
흙왯꽝鑒：
********************************************************************************
*/
INT8U   RTEEPROMread(INT16U addr)
{
    INT8U data;
    INT8U temp_l,temp_h;
    temp_l=(INT8U)(addr&0xFF);
    temp_h=(INT8U)((addr>>8)&0xFF);
    StartEERAM();
    OutputData(IIC_WRITE);
    AckEERAM();
    OutputData(temp_h);
    AckEERAM();
    OutputData(temp_l);
    AckEERAM();
    StartEERAM();
    OutputData(IIC_READ);
    AckEERAM();
    data=InputData();
    AckEERAM();
    StopEERAM();
    return(data);
}
